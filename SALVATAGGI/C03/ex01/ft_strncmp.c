int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	char	u1;
	char	u2;

	while (n-- > 0)
	{
		u1 = *s1++;
		u2 = *s2++;
		if (u1 != u2)
			return (u1 - u2);
		if (u1 == '\0')
			return (0);
	}
	return (0);
}
